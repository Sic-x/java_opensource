package com.xmh.crm.domain.resources;

import com.xmh.basic.domain.BaseDomain;

/**
 * 系统资源
 */
public class Resources extends BaseDomain {

    /**
     * 资源名称
     */
    private String name;

    /**
     * 资源地址
     */
    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
